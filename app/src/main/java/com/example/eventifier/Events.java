package com.example.eventifier;

public class Events {
    String name, desc, price, date, time, committee;

    public Events(String name, String desc, String price, String date, String time, String committee) {
        this.name = name;
        this.desc = desc;
        this.price = price;
        this.date = date;
        this.time = time;
        this.committee = committee;
    }
}

